package codingTest.day1;

import java.util.Scanner;

public class Test2 {
//1부터 10까지의 합
	public static void main(String[] args) {
		int n = 1, sum=0;
		
		while (n<=10) {
			n++;			
			sum+=n;
			
		}
		System.out.println();
		System.out.println("1부터 10까지의 합은 : "+sum);

	}

}
//i+=2의 의미는 i=i+2 

//i++의 의미 i=i+1 

//i--의  의미 i=i-1 
